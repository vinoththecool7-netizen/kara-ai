def test_parse_26as_missing_part_d_no_crash():
    """A 26AS PDF without a refund section must not crash."""
    import io

    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import getSampleStyleSheet
    from reportlab.platypus import Paragraph, SimpleDocTemplate

    buf = io.BytesIO()
    styles = getSampleStyleSheet()
    doc = SimpleDocTemplate(buf, pagesize=A4)
    story = [
        Paragraph("<b>FORM 26AS</b>", styles["Heading1"]),
        Paragraph("PAN: ABCDE1234F", styles["Normal"]),
        Paragraph("Assessment Year: 2025-26", styles["Normal"]),
        Paragraph("<b>PART A</b>", styles["Heading2"]),
        Paragraph("No data available.", styles["Normal"]),
    ]
    doc.build(story)
    pdf_bytes = buf.getvalue()

    result = parse_form_26as(pdf_bytes)
    assert isinstance(result, Form26ASDocument)
    assert result.refunds == []


