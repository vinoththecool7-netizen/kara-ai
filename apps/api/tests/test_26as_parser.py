class TestParse26ASCurrencyFormatting:
    """All monetary amounts must be stored as integer paise."""

    @pytest.fixture(scope="class")
    def doc(self) -> Form26ASDocument:
        return parse_form_26as(build_26as_pdf())

    def test_tds_deducted_is_int(self, doc: Form26ASDocument):
        for entry in doc.tds_on_salary:
            assert isinstance(entry.tax_deducted, int)

    def test_tds_deposited_is_int(self, doc: Form26ASDocument):
        for entry in doc.tds_on_salary:
            assert isinstance(entry.tax_deposited, int)

    def test_refund_amount_is_int(self, doc: Form26ASDocument):
        for refund in doc.refunds:
            assert isinstance(refund.refund_amount, int)

    def test_amounts_non_negative(self, doc: Form26ASDocument):
        for entry in doc.tds_on_salary:
            assert entry.tax_deducted >= 0
            assert entry.amount_paid_credited >= 0

    def test_totals_computed(self, doc: Form26ASDocument):
        expected_salary_tds = sum(e.tax_deducted for e in doc.tds_on_salary)
        assert doc.totals.total_tds_salary == expected_salary_tds


