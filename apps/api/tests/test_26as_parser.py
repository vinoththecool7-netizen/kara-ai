from __future__ import annotations

import pytest

from kara_api.parsers.twenty_six_as import Form26ASDocument, parse_form_26as
from tests.fixtures.twenty_six_as_factory import build_26as_pdf

# ---------------------------------------------------------------------------
# PART A — Salary TDS
# ---------------------------------------------------------------------------


class TestParse26ASPartASalary:
    """PART A table must be parsed into tds_on_salary entries."""

    @pytest.fixture(scope="class")
    def doc(self) -> Form26ASDocument:
        return parse_form_26as(
            build_26as_pdf(
                employer_tan="MUMB12345A",
                tds_salary_amount=1200000,
                tds_salary_deducted=185000,
            )
        )

    def test_returns_form26as_document(self, doc: Form26ASDocument):
        assert isinstance(doc, Form26ASDocument)

    def test_tds_on_salary_populated(self, doc: Form26ASDocument):
        assert len(doc.tds_on_salary) >= 1

    def test_employer_tan_present(self, doc: Form26ASDocument):
        tans = [e.deductor_tan for e in doc.tds_on_salary]
        assert "MUMB12345A" in tans

    def test_tax_deducted_in_paise(self, doc: Form26ASDocument):
        # 185,000 rupees → 18,500,000 paise
        total = sum(e.tax_deducted for e in doc.tds_on_salary)
        assert total == 18500000

    def test_amount_paid_in_paise(self, doc: Form26ASDocument):
        # 1,200,000 rupees → 120,000,000 paise
        total = sum(e.amount_paid_credited for e in doc.tds_on_salary)
        assert total == 120000000

    def test_source_is_pdf(self, doc: Form26ASDocument):
        assert doc.source == "pdf"


